/* example program for Ternary-operator*/

package sample1;
public class Ternaryoperator {
public static void main(String[] args) {
int a=5,b=5;
String result=(a==5)?"Equal": "Not equal";
System.out.println(result); 
result=(b==10)?"Equal":"Not equal";
System.out.println(result);
}
}
/*OUTPUT:
Equal
Not equal
-VIKRAM R*/