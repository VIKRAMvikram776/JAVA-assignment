/*Example program for Relational-operators*/


package sample1;
public class Relationaloperators {
public static void main(String[] args) {
int num = 12, num2=7;
System.out.println("The values of num and num2 are " + num + " " + num2 + " respectively");
System.out.println("The value returned when num>num2 is checked " +(num > num2));
System.out.println("The value returned when num<num2 is checked " +(num < num2)); 
System.out.println("The value returned when num>=num2 is checked" +(num >= num2));
System.out.println("The value returned when num<=num2 is checked" +(num <= num2));
System.out.println("The value returned when num==num2 is checked" +(num == num2));
System.out.println("The value returned when num!=num2 is checked" +(num != num2));
}
}

