/*Example program for bitwise-operators*/

package sample1;
public class Bitwiseoperators {
public static void main(String[] args) 
{ 
int num=7,num2=12;
System.out.println(num&num2);
System.out.println(num|num2);
System.out.println(num^num2);
System.out.println(~num);
}
}
/*OUTPUT
4
15
11
-8
-VIKRAM R*/